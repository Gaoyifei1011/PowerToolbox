namespace PowerToolbox.Extensions.DataType.Class
{
    /// <summary>
    /// 系统信息
    /// </summary>
    public class SystemInformation
    {
        /// <summary>
        /// 主机名称
        /// </summary>
        public string HostName { get; set; }

        /// <summary>
        /// 操作系统名称
        /// </summary>
        public string OperatingSystemName { get; set; }

        /// <summary>
        /// 操作系统版本
        /// </summary>
        public string OperatingSystemVersion { get; set; }

        /// <summary>
        /// 操作系统制造商
        /// </summary>
        public string OperatingSystemManufacturer { get; set; }

        /// <summary>
        /// 产品 ID
        /// </summary>
        public string ProductID { get; set; }

        /// <summary>
        /// 初始安装日期
        /// </summary>
        public string FirstInstalledDate { get; set; }

        /// <summary>
        /// 系统启动时间
        /// </summary>
        public string SystemBootTime { get; set; }

        /// <summary>
        /// 系统制造商
        /// </summary>
        public string SystemManufacturer { get; set; }

        /// <summary>
        /// 系统型号
        /// </summary>
        public string SystemModel { get; set; }

        /// <summary>
        /// 系统架构
        /// </summary>
        public string SystemArchitecture { get; set; }

        /// <summary>
        /// 系统 SKU
        /// </summary>
        public string SystemSKU { get; set; }

        /// <summary>
        /// 处理器
        /// </summary>
        public string Processor { get; set; }

        /// <summary>
        /// BIOS 版本
        /// </summary>
        public string BIOSVersion { get; set; }

        /// <summary>
        /// SMBIOS 版本
        /// </summary>
        public string SMBIOSVersion { get; set; }

        /// <summary>
        /// 嵌入式控制器版本
        /// </summary>
        public string EmbeddedControllerVersion { get; set; }

        /// <summary>
        /// BIOS 模式
        /// </summary>
        public string BIOSMode { get; set; }

        /// <summary>
        /// 主板制造商
        /// </summary>
        public string MainboardManufacturer { get; set; }

        /// <summary>
        /// 主板产品
        /// </summary>
        public string MainboardProduct { get; set; }

        /// <summary>
        /// 主板版本
        /// </summary>
        public string MainboardVersion { get; set; }

        /// <summary>
        /// Windows 目录
        /// </summary>
        public string WindowsDirectory { get; set; }

        /// <summary>
        /// 系统目录
        /// </summary>
        public string SystemDirectory { get; set; }

        /// <summary>
        /// 启动设备
        /// </summary>
        public string BootDevice { get; set; }

        /// <summary>
        /// 区域设置
        /// </summary>
        public string RegionSettings { get; set; }

        /// <summary>
        /// 时区
        /// </summary>
        public string TimeZone { get; set; }

        /// <summary>
        /// 已安装的物理内存
        /// </summary>
        public string InstalledPhysicalMemory { get; set; }

        /// <summary>
        /// 总物理内存
        /// </summary>
        public string TotalPhysicalMemory { get; set; }

        /// <summary>
        /// 可用物理内存
        /// </summary>
        public string AvailablePhysicalMemory { get; set; }

        /// <summary>
        /// 总的虚拟内存
        /// </summary>
        public string TotalVirtualMemory { get; set; }

        /// <summary>
        /// 可用虚拟内存
        /// </summary>
        public string AvailableVirtualMemory { get; set; }

        /// <summary>
        /// 页面文件位置
        /// </summary>
        public string PageFilePosition { get; set; }

        /// <summary>
        /// 页面文件大小
        /// </summary>
        public string PageFileSize { get; set; }
    }
}
